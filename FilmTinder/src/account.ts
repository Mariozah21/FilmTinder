export default class Account {
    nameAccount: string;
    age: string;
    gender: string;
    surname: string;
    email: string;
    password: string;
    constructor(nameAccount: "", age: "", gender: "", surname: "", email: "", password: "") {
      this.nameAccount = nameAccount;
      this.age = age;
      this.gender = gender;
      this.surname = surname;
      this.email = email;
      this.password = password;
    }
  }

